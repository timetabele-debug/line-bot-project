# ===== JSON操作 =====
def load_users():
    if not os.path.exists(DATA_FILE):
        return {}
    with open(DATA_FILE, "r", encoding="utf-8") as f:
        return json.load(f)

def save_users(data):
    with open(DATA_FILE, "w", encoding="utf-8") as f:
        json.dump(data, f, ensure_ascii=False, indent=2)

# ===== Flex生成 =====
def button_flex(title, buttons):
    return {
        "type": "bubble",
        "body": {
            "type": "box",
            "layout": "vertical",
            "contents": [
                {"type": "text", "text": title, "weight": "bold", "size": "lg"},
                *[
                    {
                        "type": "button",
                        "action": {"type": "message", "label": b, "text": b},
                        "style": "primary",
                        "margin": "md"
                    } for b in buttons
                ]
            ]
        }
    }

# ===== Webhook =====
@app.route("/callback", methods=["POST"])
def callback():
    signature = request.headers.get("X-Line-Signature")
    body = request.get_data(as_text=True)

    try:
        handler.handle(body, signature)
    except Exception as e:
        print(e)

    return "OK", 200


# ===== メッセージ処理 =====
@handler.add(MessageEvent, message=TextMessage)
def handle_message(event):
    text = event.message.text.strip()
    user_id = event.source.user_id
    users = load_users()

    # --- 個人情報登録 ---
    if text == "個人情報登録":
        flex = button_flex("学校を選択", list(SCHOOLS.keys()))
        line_bot_api.reply_message(
            event.reply_token,
            FlexSendMessage("学校選択", flex)
        )
        return

    # 学校選択
    if text in SCHOOLS:
        users[user_id] = {"school": text}
        save_users(users)
        flex = button_flex("学年を選択", list(SCHOOLS[text].keys()))
        line_bot_api.reply_message(event.reply_token, FlexSendMessage("学年選択", flex))
        return

    # 学年選択
    if user_id in users and "school" in users[user_id]:
        school = users[user_id]["school"]
        if text in SCHOOLS[school]:
            users[user_id]["grade"] = text
            save_users(users)
            flex = button_flex("クラスを選択", SCHOOLS[school][text])
            line_bot_api.reply_message(event.reply_token, FlexSendMessage("クラス選択", flex))
            return

    # クラス選択
    if user_id in users and "school" in users and "grade" in users[user_id]:
        school = users[user_id]["school"]
        grade = users[user_id]["grade"]
    if text in SCHOOLS[school][grade]:
        users[user_id]["class"] = text
        save_users(users)
        line_bot_api.reply_message(
            event.reply_token,
            TextSendMessage(text="登録が完了しました。\n「時間割」をご利用ください。")
        )
        return


    # --- 時間割 ---
    if text == "時間割":
        if user_id not in users or "class" not in users[user_id]:
            line_bot_api.reply_message(
                event.reply_token,
                TextSendMessage(text="個人情報の登録をしてください。")
            )
            return
        flex = button_flex("曜日を選択", ["月", "火", "水", "木", "金"])
        line_bot_api.reply_message(event.reply_token, FlexSendMessage("曜日選択", flex))
        return

    # 曜日選択
    if text in ["月", "火", "水", "木", "金"]:
        if user_id not in users or "class" not in users[user_id]:
            return
        school = users[user_id]["school"]
        cls = users[user_id]["class"]
        
        lessons = TIMETABLE.get(school, {}).get(cls, {}).get(text)

        if not lessons:
            return
        msg = f"{cls} {text}曜\n"
        for i, s in enumerate(lessons, 1):
            msg += f"{i}限：{s}\n"
        line_bot_api.reply_message(event.reply_token, TextSendMessage(text=msg.strip()))
        return

